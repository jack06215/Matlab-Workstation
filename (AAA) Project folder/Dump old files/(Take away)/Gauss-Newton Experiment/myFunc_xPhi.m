function [ result ] = myFunc_xPhi(x,y,theta,phi,foc)
result = (cos(theta)*sin(theta)*(foc*cos(phi) - foc*y*sin(phi))*...
        (sin(phi) + y*cos(phi)))/(cos(theta)*(cos(phi) - y*sin(phi))+...
        x*sin(theta))^2 - (sin(theta)*(foc*sin(phi) + foc*y*cos(phi)))...
        /(cos(theta)*(cos(phi) - y*sin(phi)) + x*sin(theta));
end

